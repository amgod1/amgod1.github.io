function finalMusicPlay() {

    if (yourScore == 0 || yourScore == 1 ) {
        soBad.play()
    }

    if (yourScore == 2 || yourScore == 3 || yourScore == 4 || yourScore == 5) {
        soSo.play()
    }

    if (yourScore > 5 && yourScore < 10) {
        finalMusic.play()
    }

    if (yourScore == 10) {
        goatMusic.play()
    }
}

function setAnswers () {

    let randId = Math.floor(Math.random() * 4 + 1);

    document.getElementById("answer_"+randId+"").firstChild.nodeValue = images[z].author;

    
    for (let i = 1; i < 5; i++){    
    
        let randomAuthors = uniqueAuthors[Math.floor(Math.random() * 92)];
        
        while (randomAuthors === images[z].author) {
            randomAuthors = uniqueAuthors[Math.floor(Math.random() * 92)]
        }
    
        if (i !== randId) {
            document.getElementById("answer_"+i+"").firstChild.nodeValue = randomAuthors;
        }
    }
}

